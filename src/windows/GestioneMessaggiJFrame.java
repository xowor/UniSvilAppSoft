    private void caricaStudenti(){
        this.studenti = Studente.getStudenti();
        for (Studente studente : this.studenti){
            this.studentiJComboBox.addItem(studente);
            this.propostaStudentiJComboBox.addItem(studente);
        }
    }
    
    private void caricaSistemi(){
        this.sistemiDiCifratura = SistemaDiCifratura.caricaSistemiCifratura(this.studente);
        for (SistemaDiCifratura sistemaDiCifratura : this.sistemiDiCifratura){
            this.propostaSistemiJComboBox.addItem(sistemaDiCifratura);
        }
    }
    
    
    private void riceviMessaggi() {
        ArrayList<Messaggio> messaggi = CommunicationController.elencaMessaggiRicevuti(this.studente);
        DefaultListModel model = new DefaultListModel();
        for (Messaggio messaggio: messaggi){
            model.addElement(messaggio);
        }
        this.ricevutiJList.setModel(model);
        
                
        messaggi = CommunicationController.getMessaggi();
        model = new DefaultListModel();
        for (Messaggio messaggio: messaggi){
            model.addElement(messaggio);
        }
        this.intercettaJList.setModel(model);
    }
    
    private void caricaProposte(){
        Studente destinatario = (Studente) this.studentiJComboBox.getSelectedItem();
        ArrayList<Proposta> proposte = CommunicationController.getProposte(this.studente);
        DefaultListModel model = new DefaultListModel();
        for (Proposta proposta : proposte){
            model.addElement(proposta);
        }
        this.proposteJList.setModel(model);
        
        //this.sistemiJComboBox.removeAllItems();
        Proposta proposta;
        proposte = CommunicationController.getProposteAccettate(this.studente, destinatario);
        if (proposte.size() > 0){
            proposta = proposte.get(0);
            this.sistemaDiCifratura = proposta.getSistemaDiCifratura();
            if (this.sistemaDiCifratura != null){
                this.sistemaTextField.setText(sistemaDiCifratura.toString());
            } else {
                this.sistemaTextField.setText("Sistema di cifratura non disponbile");
            }
        } else {
            this.sistemaTextField.setText("Nessuna proposta accettata");
        }
    }
    
    private void caricaBozze(){
        this.bozzeJComboBox.removeAllItems();
        ArrayList<Messaggio> messaggi = CommunicationController.elencaMessaggiBozza(this.studente.getId());
        for (Messaggio messaggio: messaggi){
            this.bozzeJComboBox.addItem(messaggio);
        }
    } 
    
    
    
